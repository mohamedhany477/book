const mongoose = require('mongoose');

const eventSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    trim: true
  },
  category: {
    type: String,
    trim: true
  },
  date: {
    type: Date,
    required: true
  },
  venue: {
    type: String,
    trim: true
  },
  price: {
    type: Number,
    required: true,
    min: 0
  },
  image: {
    type: String, 
    trim: true
  }
}, {
  timestamps: true  
});

module.exports = mongoose.model('Event', eventSchema);
